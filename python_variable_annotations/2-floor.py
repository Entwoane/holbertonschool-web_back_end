#!/usr/bin/env python3
"""
This module provides a function to compute the floor
of a floating-point number.
"""

import math


def floor(n: float) -> int:
    """
    Return the floor of a floating-point number.

    Args:
        n (float): The number to compute the floor of.

    Returns:
        int: The floor of the input number.
    """
    return math.floor(n)
